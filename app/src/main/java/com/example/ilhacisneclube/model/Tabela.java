package com.example.ilhacisneclube.model;

public class Tabela {

    private String timeUm, timeDois, timeTres, timeQuatro, timeCinco, timeSeis, timeSete, timeOito;
    private String nomeCampeonato;
    private String tituloTabela;
    private String nomeTime;

    public Tabela() {

    }

    public String getNomeCampeonato() {
        return nomeCampeonato;
    }

    public void setNomeCampeonato(String nomeCampeonato) {
        this.nomeCampeonato = nomeCampeonato;
    }

    public String getTituloTabela() {
        return tituloTabela;
    }

    public void setTituloTabela(String tituloTabela) {
        this.tituloTabela = tituloTabela;
    }

    public String getNomeTime() {
        return nomeTime;
    }
    public void setNomeTime(String nomeTime) {
        this.nomeTime = nomeTime;
    }


    public String getTimeUm() {
        return timeUm;
    }

    public void setTimeUm(String timeUm) {
        this.timeUm = timeUm;
    }

    public String getTimeDois() {
        return timeDois;
    }

    public void setTimeDois(String timeDois) {
        this.timeDois = timeDois;
    }

    public String getTimeTres() {
        return timeTres;
    }

    public void setTimeTres(String timeTres) {
        this.timeTres = timeTres;
    }

    public String getTimeQuatro() {
        return timeQuatro;
    }

    public void setTimeQuatro(String timeQuatro) {
        this.timeQuatro = timeQuatro;
    }

    public String getTimeCinco() {
        return timeCinco;
    }

    public void setTimeCinco(String timeCinco) {
        this.timeCinco = timeCinco;
    }

    public String getTimeSeis() {
        return timeSeis;
    }

    public void setTimeSeis(String timeSeis) {
        this.timeSeis = timeSeis;
    }

    public String getTimeSete() {
        return timeSete;
    }

    public void setTimeSete(String timeSete) {
        this.timeSete = timeSete;
    }

    public String getTimeOito() {
        return timeOito;
    }

    public void setTimeOito(String timeOito) {
        this.timeOito = timeOito;
    }
}
